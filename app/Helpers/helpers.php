if (! function_exists('formatLog')) {
    function formatLog($message)
    {
        $pattern   = '/(?=\[\d{4}-\d{2}-\d{2} \d{2}:\d{2}:\d{2}\])/';
        $formatted = preg_replace($pattern, '<br>', trim($message));
